# A list of numbers (cubes of numbers from 1 to 9). Let us do some slicing
cubes = [1, 8, 27, 64, 125, 216, 343, 512, 729]

# Print first three
print("The first three items are: ")
print(cubes[0:3])

# Print middle three
print("The middle three items are: ")
print(cubes[3:6])

# Print last three
print("The last three items are: ")
print(cubes[-3:])
